//⽇历制作
#include <algorithm>
#include <cstdio>

using namespace std;

// 每个月的天数，days[0]占位不用，days[1]~days[12]对应1~12月
// 2025年不是闰年，2月有28天
int days[20] = {0, 31, 28, 31, 30, 31, 30, 31, 31, 30, 31, 30, 31};

int main() {
    int m;
    scanf("%d", &m);                          // 读入月份 m
    
    // 输出表头：星期一到星期日
    printf("MON TUE WED THU FRI SAT SUN\n");
    
    int d = days[m];                          // 获取该月的总天数
    
    // ========== 计算该月1号是星期几 ==========
    // 已知：2025年9月1日是星期一（w=1）
    // 通过从9月向目标月份递推，计算1号的星期
    int w = 1;                                // w: 1=周一, 2=周二, ..., 7=周日
    
    if (m > 9) {
        // 目标月份在9月之后：从9月开始往后累加天数
        // 每过一个月，星期偏移 (该月天数-1) 天（因为1号到下个月1号）
        for (int i = 9; i < m; i++) 
            w = (w + days[i] - 1) % 7 + 1;   // 递推公式，保持w在1~7范围
    } else if (m < 9) {
        // 目标月份在9月之前：从8月开始往前倒推天数
        for (int i = 8; i >= m; i--) 
            w = ((w - days[i]) % 7 + 7) % 7; // 倒推：减去该月天数
        if (w == 0) w = 7;                   // 修正：0表示周日，应转为7
    }
    // m==9时w保持为1，无需处理
    
    // ========== 输出日历主体 ==========
    
    // 先输出1号前面的空格（对齐到正确的星期几）
    // 每个日期占3位宽度，1号前要空出 (w-1) 个位置
    for (int i = 1; i < w; i++) printf("   ");
    
    // 依次输出该月的每一天
    for (int i = 1; i <= d; i++) {
        printf("%3d", i);                     // 每个日期占3位宽度，右对齐
        
        w = w % 7 + 1;                        // 更新到下一天的星期
        
        // 判断是否需要换行：
        // 1. 如果下一天是周一（w==1），说明当前行已满7天
        // 2. 如果已经输出完最后一天（i == d），结束输出
        if (w == 1 || i == d)
            printf("\n");                     // 换行
        else
            printf(" ");                      // 同行内用空格分隔
    }
    
    return 0;
}
