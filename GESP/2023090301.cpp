#include<iostream>                 // 包含输入输出流库，用于cin和cout
using namespace std;               // 使用标准命名空间，避免重复写std::

int jar[1000];                     // 定义全局整型数组jar，大小为1000，模拟储蓄罐

int main() {                       // 主函数入口
    int n = 0, d = 0;              // 定义整数n(储蓄罐数量)和d(存款天数)，初始化为0
    cin >> n >> d;                 // 从输入读取储蓄罐数量和存款天数
    
    for(int i = 0; i < n; i++) {   // 循环初始化：将前n个储蓄罐金额清零
        jar[i] = 0;                // 每个储蓄罐初始金额为0
    }
    
    for(int i = 1; i <= d; i++) {  // 循环d天，i表示第几天（从1开始）
        int a = 0;                 // 定义整数a，表示要存款的储蓄罐编号
        cin >> a;                  // 从输入读取要存款的储蓄罐编号
        jar[a] += i;               // 关键操作：第i天向a号储蓄罐存入i元（天数即金额）
    }
    
    cout << jar[0];                // 输出0号储蓄罐的最终金额
    for(int i = 0; i < n; i++) {   // 循环输出1到n-1号储蓄罐的金额
        cout << " " << jar[i];     // 用空格分隔，输出每个储蓄罐的金额
    }
    cout << endl;                  // 输出换行符
    
    return 0;                      // 返回0，表示程序正常结束
}

