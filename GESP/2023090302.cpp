//4018：【GESP2309三级】进制判断
#include <iostream>
using namespace std;

int main() {
    int n = 0;
    cin >> n;  // 读取需要判断的数字个数
    
    for (int i = 0; i < n; i++) {
        char str[11];  // 定义字符数组存储输入的字符串(最大长度10+1个结束符)
        cin >> str;    // 读取当前需要判断的字符串
        
        char max = '0';  // 初始化最大字符为'0',用于记录字符串中出现的最大字符
        
        // 遍历字符串找出ASCII值最大的字符
        // 字符的ASCII值: '0'-'9' < 'A'-'F',所以找最大字符即可判断是否合法
        for (int i = 0; str[i] != '\0'; i++) {
            if (str[i] > max) {// 如果当前字符的ASCII值大于最大字符的ASCII值
                max = str[i];  // 更新最大字符
            }
        }
        
        // 判断并输出四种进制可能性:
        // 二进制: 最大字符必须 ≤ '1' (只能含0,1)
        // 八进制: 最大字符必须 ≤ '7' (只能含0-7)
        // 十进制: 最大字符必须 ≤ '9' (只能含0-9)
        // 十六进制: 最大字符必须 ≤ 'F' (只能含0-9,A-F)
        // max <= 'X' 为布尔表达式,成立输出1,否则输出0
        cout << (max <= '1') << " "  // 是否可能是二进制
             << (max <= '7') << " "  // 是否可能是八进制
             << (max <= '9') << " "  // 是否可能是十进制
             << (max <= 'F') << endl; // 是否可能是十六进制
    }
    
    return 0;
}
