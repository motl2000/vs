//4180：【GESP2603六级】完全二叉树
#include <cstdio>
#include <algorithm>

using namespace std;

const int N = 1e5 + 5;  // 最大结点数 + 5的安全余量

int n;                  // 树的结点总数
int l[N], r[N];         // l[i], r[i] 分别表示结点i的左、右儿子编号，0表示不存在
int mn[N], mx[N], chk[N];  
// mn[u]: 以u为根的子树的最小深度（到最近叶子的距离）
// mx[u]: 以u为根的子树的最大深度（到最远叶子的距离）  
// chk[u]: 以u为根的子树是否为完全二叉树（1是，0否）
int ans;                // 答案：完全二叉树的子树个数

void dfs(int u) {
    chk[u] = 1;         // 先假设当前子树是完全二叉树，后续验证不通过则置0
    
    if (!u)             // 递归边界：空结点（编号为0）
        return;         // 空树直接返回，其mn[0]=mx[0]=0, chk[0]=1（由全局初始化保证）
    
    // ========== 后序遍历：先处理左右子树 ==========
    dfs(l[u]);          // 递归处理左子树
    dfs(r[u]);          // 递归处理右子树
    
    // ========== 利用子树信息，判断当前子树 ==========
    
    // 条件1：左右子树都必须是完全二叉树
    // 若任一子树不是完全二叉树，则当前子树也一定不是
    chk[u] &= chk[l[u]] & chk[r[u]];
    
    // 计算当前子树的最小深度和最大深度
    // 当前深度 = 1（自己） + 子树的深度
    mn[u] = 1 + min(mn[l[u]], mn[r[u]]);  // 最小深度取左右子树的较小值
    mx[u] = 1 + max(mx[l[u]], mx[r[u]]);  // 最大深度取左右子树的较大值
    
    // 条件2：完全二叉树的核心性质 — 左子树"填满"程度要 >= 右子树
    // mn[l[u]]: 左子树最小深度（左子树最浅叶子在哪层）
    // mx[r[u]]: 右子树最大深度（右子树最深叶子在哪层）
    // 若左子树最浅处 >= 右子树最深处，说明右子树是满的，叶子靠左分布
    chk[u] &= mn[l[u]] >= mx[r[u]];
    
    // 条件3：整体高度差不超过1（保证最后一层叶子连续，没有断层）
    // mn[u] >= mx[u] - 1 等价于 mx[u] - mn[u] <= 1
    chk[u] &= mn[u] >= mx[u] - 1;
    
    // ========== 统计答案 ==========
    ans += chk[u];      // 若当前子树是完全二叉树，答案+1
}

int main() {
    scanf("%d", &n);
    for (int i = 1; i <= n; i++)
        scanf("%d%d", &l[i], &r[i]);  // 读入每个结点的左右儿子
    
    // 全局数组初始化为0，所以空结点0的 mn[0]=mx[0]=0, chk[0]=1 自动满足
    
    dfs(1);             // 从根结点开始DFS
    
    printf("%d\n", ans);
    return 0;
}
